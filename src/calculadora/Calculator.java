    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        secondOperand = Double.parseDouble(textShow.getText());
        double so = secondOperand;
       if(opI == 1){
            String res = add(fo, so);
            refreshDisplay(res);
            refreshDisplay(text);
       }
       
       else if(opI == 2){
            String res = sub(fo, so);
            refreshDisplay(res);
            refreshDisplay(text);
       }
       
       else if(opI == 3){
            String res = mult(fo, so);
            refreshDisplay(res);
            refreshDisplay(text);
       }
       
       else if(opI == 4){
            String res = div(fo, so);
            refreshDisplay(res);
            refreshDisplay(text);
       }
       
    }//GEN-LAST:event_jButton11ActionPerformed

