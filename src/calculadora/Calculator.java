    public String add(double fOpr, double SOpr){
        
        res = fOpr + SOpr;
        text = Double.toString(res);
        return text;
    }
    
    public String sub(double fOpr, double SOpr){
        res = fOpr - SOpr;
        text = Double.toString(res);
        return text;
    }
    
    public String mult(double fOpr, double SOpr){
        res = fOpr * SOpr;
        text = Double.toString(res);
        return text;
    }
    
    public String div(double fOpr, double SOpr){
        res = fOpr / SOpr;
        text = Double.toString(res);
        return text;
    }

